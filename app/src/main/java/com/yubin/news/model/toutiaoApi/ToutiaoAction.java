public class ToutiaoAction {

    private int action;
    private ToutiaoExtra extra;
    private String desc;

    public int getAction() {
        return action;
    }

    public void setAction(int action) {
        this.action = action;
    }

    public ToutiaoExtra getExtra() {
        return extra;
    }

    public void setExtra(ToutiaoExtra extra) {
        this.extra = extra;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }



}
